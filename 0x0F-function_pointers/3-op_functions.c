#include "3-calc.h"

/**
 * op_add - calculates sum of 2 int
 * @a: 1st int
 * @b: 2nd int
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two int
 * @a: 1st int
 * @b: 2nd int
 *Return: difference of the 2 int
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - computes the product of two int
 * @a: 1st int
 * @b: 2nd int
 * Return: product
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - computes devision of two int
 * @a: 1st int
 * @b: 2nd int
 * Return: devision
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - computes the modulous of two int
 * @a: 1st int
 * @b: 2nd int
 * Return: remainder of devision
 */

int op_mod(int a, int b)
{
	return (a % b);
}
